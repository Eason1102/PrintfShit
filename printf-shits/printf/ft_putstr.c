#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(NULL)\n", 8));
	while (s[i])
	{
		write(1, s[i], 1);
		i++;
	}
	return (i);
}
